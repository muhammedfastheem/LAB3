// first include library
#include <iostream>
using namespace std;
// write the main function
int main() {
	// declaring variables of all types
	int x,y;// declaring variables together
	float a;
	char s;
	bool k;
	double c;
	// defining the variables
	x = 6;
	y = 2;
	a = 1.5;
	s = 'b';
	k = 0;
	c = 1.6;
	// displaying the sizes of datatypes
	cout << "size of x = " << sizeof(x) <<endl;
	cout << "size of y = " << sizeof(y) <<endl;
	cout << "size of a = " << sizeof(a) <<endl;
	cout << "size of s = " << sizeof(s) <<endl;
	cout << "size of k = " << sizeof(k) <<endl;
	cout << "size of c = " << sizeof(c) <<endl;
	return 0;
}
	
	
